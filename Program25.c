// Display Jay Ganesh on console 4 time (with the help of Iteration approach) .

#include<stdio.h>

int main()
{
    int iCnt = 0 ;

    for(iCnt = 1 ; iCnt <= 4 ; iCnt++)
    {
        printf("Jay Ganesh... \n");
    }

    return 0 ;
}

/*
    OUTPUT

    Jay Ganesh...
    Jay Ganesh...
    Jay Ganesh...
    Jay Ganesh...
*/