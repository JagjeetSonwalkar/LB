// Display Jay Ganesh on console 11 times with the help of iteration approch 

#include<stdio.h>

void Display()
{
    int iCnt = 0 ;

    for(iCnt = 1 ; iCnt <= 11 ; iCnt++)
    {
        printf("Jay Ganesh... \n");
    }
}

int main()
{
    Display();

    return 0 ;
}

