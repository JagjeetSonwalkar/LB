// Display the number from 1 to 5 with the help of iteration approch.

#include<stdio.h>

int main()
{
    int iCnt = 0 ;

    for(iCnt = 1 ; iCnt <= 5 ; iCnt++ )
    {
        printf("%d \n",iCnt);   
    }

    return 0 ;
}

/*
    OUTPUT

    1
    2
    3
    4
    5
    
*/