// // Display Jay Ganesh on console 5 times with the help of iteration approch (While loop).

#include<stdio.h>

int main()
{
    int iCnt = 0 ;

    iCnt = 1 ;
    while(iCnt <= 5)
    {
        printf("Jay Ganesh... \n");
        iCnt ++ ;
    } 

    return 0 ;
}

/*
    OUTPUT

    Jay Ganesh...
    Jay Ganesh...
    Jay Ganesh...
    Jay Ganesh...
    Jay Ganesh...

*/