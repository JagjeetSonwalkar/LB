// Accept the number from the user and display all its factor .

#include<stdio.h>

void Display (int iValue)
{
    int iCnt = 0 ;

    printf("Factor of %d is \n ",iValue);

    for(iCnt = 1 ; iCnt <= iValue/2; iCnt++ )       // Time complexity O(N/2)
    {
        if( ( iValue % iCnt ) == 0)
        {
            printf("%d \t",iCnt);
        }
    }
}

int main()
{
    int iNum = 0 ;

    printf("Enter the number : \n");
    scanf("%d",&iNum);

    Display(iNum);

    return 0 ;
}

